  <h2>Creating</h2>
  <ol>
    <li><a href="#fbx">Buy/Build FBX</a></li>
    <li><a href="#host">Host your FBX</a></li>
    <li><a href="#load">Load a script</a></li>
    <li><a href="#place">Place content</a></li>
  </ol>
  <p>
    <a name="fbx">1.</a>
    Buy or create, then bring your FBX into High Fidelity.
    <a href="http://mixamo.com">Mixamo</a>,
    the <a href="https://www.assetstore.unity3d.com/en/">Unity Asset Store</a>,
    <a href="http://www.daz3d.com/">Daz3D</a> and numerous other sources are great 
    places to start. Once you have the FBX you can rig it up as well.
  </p>
  <p>
    <a name="host">2.</a> 
    Get it up in the cloud, available via a public URL. Make sure downloads will be 
    quick from anywhere in the world (e.g. S3 or equivalent)

  </p>
  <p>
    <a name="load">3.</a>
    Launch Interface and load 
    <a href="http://hifi-public.s3.amazonaws.com/scripts/editEntities.js">editEntities.js</a>
    or grow your own script — all of our scripts are JavaScript-based and easily edited.
  </p>
    <p>
    <a name="place">4.</a>
    If you’re using editEntities, click the cloud on the right to place your model into the
    metaverse.
  </p>
  <p>
    <strong>Questions?</strong>
    <a href="http://docs.highfidelity.io">Check out our documentation</a>
  </p>
